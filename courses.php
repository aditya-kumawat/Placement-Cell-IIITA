					<big><strong><b>Undergraduate Courses</b></strong></big><br>
					<ul>
						<li>B. Tech Information Technology (IT)</li>
						<li>B. Tech Electronics and Communication Engineering (ECE)</li>
					</ul>
					This course is arguably the best in the country. Its strength is its adaptability to the changing industry requirements. A student gets knowledge of the latest technologies, in addition to ample industry exposure. The students are given a choice of subject from the 6th semester itself in the form of electives. The students have ample practical knowledge owing to the mini projects taken up in their 5th, 6th and 7th semesters. Apart from this, the students are required to submit an industrial project in their 8th semester. The course includes both theory and lab sessions. The curriculum emphasizes importance of applied knowledge, and promotes state-of-the-art teaching and learning.
				</p>
				<p>
					<big><strong><b>Postgraduate Courses</b></strong></big><br>
					<ul>
						<li>Wireless Communication Engineering (WCE)</li>
						<li>Software Engineering (SE)</li>
						<li>Cyber Law and Information Security (CLIS)</li>
						<li>Intelligent Systems (IS)</li>
						<li>Robotics (RO)</li>
						<li>Human Computer Interaction (HC)</li>
						<li>Microelectronics(ME)</li>
						<li>Bioinformatics (BI) </li>
						<li>Integrated M. Tech in Biomedical Engineering. (BM)</li>
					</ul>
					IIIT Allahabad provides excellence of specialization in the field of information technology in India. The objective of M.Tech is to prepare quality professionals to work at high-end technologies in IT and to deliver future project leaders and architects, who can meet the challenges, make better use of technologies and understand the need of modern IT services. Here students are trained to work in a team, lead it, allowing exposure to the practicability and difficulties involved in the projects of varying complexities to make sure that the students have the capability to turn the dreams of business leaders and visionaries into realities.The curriculum is deigned in such a manner that student get the excellent knowledge of the specialized field. 'Thesis work' which provides an opportunity to students to create and develop new concept, techniques, methods and applications in the real world.
